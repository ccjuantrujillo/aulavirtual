              <h3>La Teledetección y el COVID 19 - Cómo pueden ayudar los satélites</h3>
              <div class="news-tags">
                <ul class="time">
                  <li><i class="fa fa-user" aria-hidden="true"></i> <a href="https://www.linkedin.com/in/yolanda-bustamante-sanchez-a5787a125/?originalSubdomain=pe" target="_blank">Por Yolanda Bustamante</a></li>
                  <li><i class="fa fa-clock-o" aria-hidden="true"></i>20/05/2020</li>
                </ul>
              </div>
              <p>La pandemia del coronavirus COVID-19 ha paralizado la vida cotidiana tal como la conocemos.  Incluso cuando se haya detenido la propagación de esa enfermedad altamente infecciosa, el mundo enfrentara enormes desafíos.</p>
			  <p>En este contexto, la teledetección o detección a distancia de las propiedades de los objetos presentes en la superficie de la Tierra, brindada por científicos con el manejo de plataformas ofrece múltiples ventajas para usar dicha información territorial para comprender y controlar los cambios que allí se llevan a cabo para realizar una correcta toma de decisiones. Por esta razón es importante impulsar esta rama del saber justo en momentos actuales en que la humanidad vive por motivo de la Pandemia COVID.</p>
			  <p>La Teledetección, motivo de nuestra investigación, es la misión satelital que realiza Copernicus Sentinel-50 (1) desde Europa para proporcionar información clave sobre la superficie terrestre a través de sensores instalados en plataformas espaciales y contribuir a arrojar nueva luz sobre los cambios sociales y económicos que allí se producen. De este modo es posible mapear los campos alrededor de las redes de transportes, puertos comerciales, la industria como refinerías de petróleo, las zonas mineras y otras áreas de interés.</p>            
			  <p>Desde nuestra posición en CECCoS creemos que las tecnologías reinantes han de contribuir a mermar la problemática existente. Creemos que la INNOVACION es clave para usar datos satelitales para ayudar a servir a la sociedad durante estos tiempos difíciles y confiamos en que nuestra propuesta llegue a instituciones como la UNIVERSIDAD, GOBIERNOS LOCALES, REGIONALES Y GOBIERNO CENTRAL, los cuales llevarán a cabo acciones consensuadas multidisciplinarias y realizar propuestas en los diferentes sectores económicos como la industria, el comercio, el transporte, la agricultura, la minería.</p>
			  <p>Por tanto, corresponde a  los profesionales especializados trabajar coordenada y colaborativamente estrategias que den debida solución a la problemática que vivimos contando con el apoyo de acertadas políticas públicas para promover cambios en los diferentes sectores de la economía peruana seriamente dañados por el Covid -19</p>
