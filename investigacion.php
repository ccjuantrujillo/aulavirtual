              <p>La pandemia del COVD-19 ha afectado nuestro país en los aspectos social, económico…. Por ello el Estado peruano ha tomado muchas medidas para lograrlo como el aislamiento, medidas profilácticas, cambio de cultura sociales (no estrechamiento de manos, no cercanía al saludar entre otros.</p>
			  <p>En esta situación se estableció la entrega de bonos a las familias, pero surgió un problema durante el cobro de estos pues los usuarios no contaban con tarjetas digitales, no estaban familiarizados con los procesos digitales lo que produjo que se aglomeraran en los bancos con tanta cercanía que se incrementó considerablemente el número de casos de Covid-19</p>
			  <p>Ya desde inicios de año el Estado peruano promulgó dos Decretos de Urgencia el 006-2020 que crea el Sistema Nacional de Transformación Digital y el 007-2020 que aprueba el marco de Confianza Digital para prevenir los riesgos digitales y determino a través de la PCM y el DL 1497 la conversión de los procesos administrativos a plataformas o servicios digitales.</p>            
			  <p>Por tanto, nuestra población requiere un proceso de transformación en sus costumbres, sus hábitos tras una nueva Cultura Organizacional que promueva la agilidad de las acciones y la colaboración con sus pares. Se precisa pues metodologías agiles para este fin que conllevara a cambios de comportamientos de los trabajadores y consumidores promoviendo la Transformación Digital de modelos de negocios más operativos.</p>
			  <p>Para tal fin se viene implementando iniciativas  que incluyen  una infraestructura tecnológica para el trabajo ubicuo (teletrabajo), el  despliegue de un ecosistema cloud adaptado a dispositivos móviles, aceleración de los sistemas de ciberseguridad, soluciones de inteligencia artificial, la robotización de los procesos, uso de plataformas de trabajo colaborativo, aprendizaje colaborativo impulsoras de la innovación y creatividad de las organizaciones, estrategias que se orientes al cliente digital (customer centric), canales digitales de interacción con el cliente, plataformas de comercio electrónico, tecnologías contactless o pagos sin contacto  (banco Continental), soluciones blockchain.</p>
			  <p>De este modo la transformación digital en nuestro país permitirá una creciente conectividad y el uso de internet lo que generará una serie de cambios dando lugar a una Metamorfosis Digital para lograr la migración masiva acorde a los 21 compromisos de la Agenda Digital al Bicentenario con cinco aspiraciones por un Perú integro, innovador, competitivo, confiable.</p>
