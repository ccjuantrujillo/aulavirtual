              <p>CECCoS, Centro de Investigación, Capacitación y Comunicación Social, fundada en diciembre 2018 se vio afectada por el COVID 19 por lo que el gobierno peruano decreto cuarentena a partir de marzo 11 del presente año. CECCoS venía haciendo su promoción de cursos de invierno, nuevos productos, nuevo material de capacitación y temas de investigación en torno al medio ambiente, sostenibilidad y otros. </p>
              <p>Se venia teniendo conocimiento del COVID 19 a través de investigaciones online en países como China, países europeos que fueron duramente golpeados, sin embargo, veíamos muy lejano la llegada a países de Sud América el arribo de este terrible mal y más específicamente en nuestro país.</p>
              <p>Se venia teniendo conocimiento del COVID 19 a través de investigaciones online en países como China, países europeos que fueron duramente golpeados, sin embargo, veíamos muy lejano la llegada a países de Sud América el arribo de este terrible mal y más específicamente en nuestro país.</p>
              <p>Esta semana que viene en que finalmente acaba este confinamiento se presentaran nuevos retos en nuestra rutina ciudadana, el distanciamiento social más estricto será al salir de nuevo a los centros de estudios o trabajo. En el transporte será diferente, bastante separados entre pasajeros, usando siempre mascarillas y atentos a no acercarnos a otras personas y dejar que ellos se acerquen demasiado, estar alertas cuando frecuentemos los lugares públicos como supermercados, bancos, paraderos, parques, calles y otros.</p>
              <p>Como siempre CECCoS ha venido realizando investigación en diferentes campos para ponerlo al alcance de sus seguidores, prepararlos para los nuevos desafíos que requiere el contexto social, ofreciendo siempre capacitación online en idiomas, música y otros campos del saber. Como es sabido la cercanía o clases presenciales serán realizadas posteriormente, por ahora daremos a conocer actividades en línea, poniendo lo mejor al alcance de la colectividad.</p>
              <p>Estamos próximos a ofrecer el proyecto Yob English for Children 2020, YECh-2020. En curso figuran talleres (Compostaje), acciones de apoyo a Aprende en Casa (copias, monografías, trípticos), asesoría en cursos de Matemáticas, Geometría, Trigonometría y otros</p>
