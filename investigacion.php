              <h3>LA BASURA ELECTRONICA</h3>
              <div class="news-tags">
                <ul class="time">
                  <li><i class="fa fa-user" aria-hidden="true"></i> <a href="https://www.linkedin.com/in/yolanda-bustamante-sanchez-a5787a125/?originalSubdomain=pe" target="_blank">Por Yolanda Bustamante</a></li>
                  <li><i class="fa fa-clock-o" aria-hidden="true"></i>24/05/2020</li>
                </ul>
              </div>
              <p>Se llama basura electrónica a todos los residuos de aparatos eléctricos y electrónicos conocidos como RAEE que han llegado al final de su vida útil como computadoras viejas, celulares, electromésticos, memorias USB, impresoras que van quedando obsoletos por el avance de la tecnología y que son tóxicos y contaminan el medio ambiente perjudicando el aire que respiramos, la tierra y el agua que bebemos</p>
			  <p>Los RAEE tiene diez categorías:<br>
				-	Grandes electrodomésticos: microondas, secadoras, lavadoras<br>
				-	Pequeños electrodomésticos: planchas, tostadoras, licuadoras<br>
				-	Equipos de informática y telecomunicaciones: computadoras, impresoras, escanea doras, teléfonos<br>
				-	Aparatos electrónicos de consumo: radios, televisores, videocámaras<br>
				-	Aparatos de alumbrado: lámparas, fluorescentes<br>
				-	Herramientas eléctricas y electrónicas: máquinas de coser, sierra, taladradoras, <br>
				-	Juguetes y equipos deportivos: trenes eléctricos, máquinas tragamonedas, consolas portátiles<br>
				-	Aparatos médicos: ventiladores pulmonares, equipos de diálisis<br>
				-	Instrumentos de vigilancia y control: detector de humos, termostatos, aparatos de medición<br>
				-	Máquinas expendedoras: expendedoras de bebidas calientes, botellas o dinero.
			  </p>
			  <p>
				Los dispositivos digitales como móviles, tabletas, portátiles en desuso se están convirtiendo en un problema para planeta se generan casi 50 millones de toneladas de chatarra tecnológica y su tasa de reciclaje es insuficiente. Al manipularlos indebidamente eliminan sustancias nocivas para la salud como el plomo, arsénico, selenio, cadmio, cromo, níquel y otras sustancias que los RAEE contiene.<br><br>
				<img src="images/rae.png" alt="News Image">
			  </p><br>
			  <p>En el Perú los RAEE se dejan a cielo abierto en la vía publica o en basureros informales…sus cables son acopiados para luego quemarlos y obtener el cobre que hay dentro generando gases cancerígenos y contaminantes. Sin embargo, constituye una oportunidad para la creación de un nuevo mercado y una nueva industria especializadas pues los RAEE tienen un valor económico importante en el mercado, por ello se viene trabajando con el MINAM y el grupo de Trabajo del sector público, privado y la sociedad civil para la elaboración y aprobación del reglamento que permitirá contar con un Plan Nacional de Gestión y Manejo de los RAE. (1)</p>
			  <p>El alcalde de Lima, Jorge Muñoz Wells, anunció la campaña TecnoRecicla Lima (Régimen Especial de Gestión de RAEE, DS 099-2019-Minam, para fomentar el reciclaje de RAEE, que activarán espacios de acopio en los 43 distritos de la ciudad para alcanzar la meta de 10 toneladas de RAEE recolectados hasta diciembre 2020, comenzar el 9 y 10 de noviembre en los distritos del cercado de Lima y Magdalena del Mar, luego VMT, Miraflores, Jesús María, Puente Piedra y SJM. Los voluntarios ambientales de la comuna estarán correctamente identificados para hacer el registro de los aparatos entregados. Para ello cuenta con el apoyo de los distritos municipales, MINAMN, Entel, Claro y Movistar, dos empresas operadoras de RAEE, San Antonio REcycling y Comimtel Recycling y tres sistemas colectivos que los producen, Aspager, RLG y Recolecc Sistema Colectivo.</p>
			  <p>La Municipalidad de Lima invita a los vecinos de la ciudad a que se sumen a esta campaña (lanzamiento de esta campaña 16.10.19) metropolitana, comprometiéndose con el cuidado del ambiente y difundiendo la entrega de sus aparatos en redes sociales con el hashtag #TecnoreciclaLima</p>
